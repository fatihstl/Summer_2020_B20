package day03_sequences_Veriables;

public class EscapeSquences {


    public static void main(String[] args) {

        System.out.println("Hello \n everyone my name is Fatih"); // ln ornegi
        System.out.println("I am \t in STL"); // lt ornegi
        System.out.println("Merhaba canim \t\t\tnasilsin"); // 3 tab yapti cunku uc kere arka arkaya yaptim.
        System.out.println("\tI am loving Programming\n");
        System.out.println(" \n \t\t\tcano");
        System.out.println(" \\The Good Place "); // bir aded almak icin
        System.out.println(" \\\\The Good Place "); // iki aded almak icin
        System.out.println(" \' cano \' ");
        System.out.println(" \" Fatih \" ");


        System.out.println("\nfatih");
        System.out.println("\t fatih");
        System.out.println("\\ fatih");
        System.out.println(" \\\\ fatih");
        System.out.println(" \' fatih\' ");
        System.out.println(" \"fatih \"");


        System.out.println(" I love \" Edward Said \"  and I like  \' Entellektuel\'");
        System.out.println(" I love \" Edward Said \"  \n I like \' Entellektuel\'");
        System.out.println(" Benim adim: \" Bay Necip\" \n babaminki: \'Fazil Bey\' ");


    }


}
