package Day08_Logicaloperators;

public class D_Logical {

    public static void main(String[] args) {

        // !: opposite operator
        boolean r4 = true == !true;
        System.out.println(r4);


        boolean r1 =  9>7; // true
        boolean r2 = !r1; //false
        System.out.println(r2);


        boolean r5 = 9>5 && 9 >10;
        System.out.println(r5); // false olur





    }

}
